			async onSubmitTap() {
				let that = this
				//跨表计算判断
				var obj;
				if((!this.ruleForm.juminzhanghao)){
					this.$utils.msg(`居民账号不能为空`);
					return
				}
				if((!this.ruleForm.mima)){
