<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
	import xiaEditor from '@/components/xia-editor/xia-editor';
	import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				shangjiamingcheng: '',
				dianputupian: '',
				zhandimianji: '',
				lianxidianhua: '',
				fuzeren: '',
				zhuxiaochanpin: '',
				dianpujianjie: '',
				discussnum: '',
				storeupnum: '',
				longitude: '',
				latitude: '',
				fulladdress: '',
				},
				// 登录用户信息
				user: {},
				ro:{
				   shangjiamingcheng : false,
				   dianputupian : false,
				   zhandimianji : false,
				   lianxidianhua : false,
				   fuzeren : false,
				   zhuxiaochanpin : false,
				   dianpujianjie : false,
				   discussnum : false,
				   storeupnum : false,
				   longitude : false,
				   latitude : false,
				   fulladdress : false,
				},
				virtualPay: false,
			}
		},
		components: {
			wPicker,
			xiaEditor,
			multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			if(options.virtualPay){
				this.virtualPay = true
			}
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取



			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`zhoubianfuwu`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='shangjiamingcheng'){
						this.ruleForm.shangjiamingcheng = obj[o];
						this.ro.shangjiamingcheng = true;
						continue;
					}
					if(o=='dianputupian'){
						this.ruleForm.dianputupian = obj[o].split(",")[0];
						this.ro.dianputupian = true;
						continue;
					}
					if(o=='zhandimianji'){
						this.ruleForm.zhandimianji = obj[o];
						this.ro.zhandimianji = true;
						continue;
					}
					if(o=='lianxidianhua'){
						this.ruleForm.lianxidianhua = obj[o];
						this.ro.lianxidianhua = true;
						continue;
					}
					if(o=='fuzeren'){
						this.ruleForm.fuzeren = obj[o];
						this.ro.fuzeren = true;
						continue;
					}
					if(o=='zhuxiaochanpin'){
						this.ruleForm.zhuxiaochanpin = obj[o];
						this.ro.zhuxiaochanpin = true;
						continue;
					}
					if(o=='dianpujianjie'){
						this.ruleForm.dianpujianjie = obj[o];
						this.ro.dianpujianjie = true;
						continue;
					}
					if(o=='discussnum'){
						this.ruleForm.discussnum = obj[o];
						this.ro.discussnum = true;
						continue;
					}
					if(o=='storeupnum'){
						this.ruleForm.storeupnum = obj[o];
						this.ro.storeupnum = true;
						continue;
					}
					if(o=='longitude'){
						this.ruleForm.longitude = obj[o];
						this.ro.longitude = true;
						continue;
					}
					if(o=='latitude'){
						this.ruleForm.latitude = obj[o];
						this.ro.latitude = true;
						continue;
					}
					if(o=='fulladdress'){
						this.ruleForm.fulladdress = obj[o];
						this.ro.fulladdress = true;
						continue;
					}
				}
			}
			this.styleChange()
			this.$forceUpdate()
			if (uni.getStorageSync('raffleType') && uni.getStorageSync('raffleType') != null) {
				uni.removeStorageSync('raffleType')
				setTimeout(() => {
					this.onSubmitTap()
				}, 300)
			}
		},
		methods: {
			dianpujianjieChange(e) {
				this.ruleForm.dianpujianjie = e
			},
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
			fulladdressTap (name='') {
				if(this.ro[name]) {
					return false
				}
				uni.chooseLocation({
					success: (data) => {
						if(name){
							this.ruleForm[name] = data.longitude + ';' + data.latitude + ';' + data.address
							this.ruleForm[name + 2] = data.address
						}else {
							this.ruleForm.fulladdress = data.address;
							this.ruleForm.longitude = data.longitude;
							this.ruleForm.latitude = data.latitude;
						}
						this.$forceUpdate()
					}
				})
			},

			// 多级联动参数




			dianputupianTap() {
				let _this = this;
				if(this.ro.dianputupian){
					return false
				}
				this.$api.upload(function(res) {
					_this.ruleForm.dianputupian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				let that = this
				//跨表计算判断
				var obj;
				if(this.ruleForm.lianxidianhua&&(!this.$validate.isMobile(this.ruleForm.lianxidianhua))){
					this.$utils.msg(`联系电话应输入手机格式`);
					return
				}
				if(this.ruleForm.discussnum&&(!this.$validate.isIntNumer(this.ruleForm.discussnum))){
					this.$utils.msg(`评论数应输入整数`);
					return
				}
				if(this.ruleForm.storeupnum&&(!this.$validate.isIntNumer(this.ruleForm.storeupnum))){
					this.$utils.msg(`收藏数应输入整数`);
					return
				}
				if(this.ruleForm.longitude&&(!this.$validate.isNumber(this.ruleForm.longitude))){
					this.$utils.msg(`经度应输入数字`);
					return
				}
				if(this.ruleForm.latitude&&(!this.$validate.isNumber(this.ruleForm.latitude))){
					this.$utils.msg(`纬度应输入数字`);
					return
				}
				//更新跨表属性
				var crossuserid;
				var crossrefid;
				var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
						if(!obj) {
							obj = uni.getStorageSync('crossObj');
						}
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
							   crossuserid=Number(uni.getStorageSync('appUserid'));
							   crossrefid=obj['id'];
							   crossoptnum=uni.getStorageSync('statusColumnName');
							   crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`zhoubianfuwu`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						uni.removeStorageSync('crossCleanType');
						return false;
					} else {
				//跨表计算
						let oet = {}
						if(this.ruleForm.id){
							await this.$api.update(`zhoubianfuwu`, this.ruleForm);
						}else{
							oet = await this.$api.add(`zhoubianfuwu`, this.ruleForm);
						}
						if(this.cross){
							uni.setStorageSync('crossCleanType',true);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
				//跨表计算
					let oet = {}
					if(this.ruleForm.id){
						await this.$api.update(`zhoubianfuwu`, this.ruleForm);
					}else{
						oet = await this.$api.add(`zhoubianfuwu`, this.ruleForm);
					}
					if(this.cross){
						uni.setStorageSync('crossCleanType',true);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				if(this.ro[str]){
					return false
				}
				this.$refs[str].show();
			},
		}
	}
</script>

