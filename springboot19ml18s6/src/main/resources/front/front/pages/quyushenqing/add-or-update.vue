	import wPicker from "@/components/w-picker/w-picker.vue";
	import xiaEditor from '@/components/xia-editor/xia-editor';
	import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				qishouzhanghao: '',
				qishouxingming: '',
				touxiang: '',
				dianhua: '',
				dingdanbianhao: '',
				peisongshijian: '',
				zhengmingcailiao: '',
				shenqingshijian: '',
				beizhu: '',
				shhf: '',
				},
				// 登录用户信息
				user: {},
				ro:{
				   qishouzhanghao : false,
				   qishouxingming : false,
				   touxiang : false,
				   dianhua : false,
				   dingdanbianhao : false,
				   peisongshijian : false,
				   zhengmingcailiao : false,
				   shenqingshijian : false,
				   beizhu : false,
				   sfsh : false,
				   shhf : false,
				},
				virtualPay: false,
			}
		},
		components: {
			wPicker,
			xiaEditor,
			multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			if(options.virtualPay){
				this.virtualPay = true
			}
			this.ruleForm.shenqingshijian =  this.$utils.getCurDateTime();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.qishouzhanghao = this.user.qishouzhanghao
			this.ro.qishouzhanghao = true;
			this.ruleForm.qishouxingming = this.user.qishouxingming
			this.ro.qishouxingming = true;
			this.ruleForm.dianhua = this.user.dianhua
			this.ro.dianhua = true;



			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`quyushenqing`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='qishouzhanghao'){
						this.ruleForm.qishouzhanghao = obj[o];
						this.ro.qishouzhanghao = true;
						continue;
					}
					if(o=='qishouxingming'){
						this.ruleForm.qishouxingming = obj[o];
						this.ro.qishouxingming = true;
						continue;
					}
					if(o=='touxiang'){
						this.ruleForm.touxiang = obj[o].split(",")[0];
						this.ro.touxiang = true;
						continue;
					}
					if(o=='dianhua'){
						this.ruleForm.dianhua = obj[o];
						this.ro.dianhua = true;
						continue;
					}
					if(o=='dingdanbianhao'){
						this.ruleForm.dingdanbianhao = obj[o];
						this.ro.dingdanbianhao = true;
						continue;
					}
					if(o=='peisongshijian'){
						this.ruleForm.peisongshijian = obj[o];
						this.ro.peisongshijian = true;
						continue;
					}
					if(o=='zhengmingcailiao'){
						this.ruleForm.zhengmingcailiao = obj[o];
						this.ro.zhengmingcailiao = true;
						continue;
					}
					if(o=='shenqingshijian'){
						this.ruleForm.shenqingshijian = obj[o];
						this.ro.shenqingshijian = true;
						continue;
					}
